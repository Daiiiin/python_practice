# strings
name = "Matthew Desabelle"
print(name)
print("Hello, " + name + ". How are you?")

# integers
age = 23
print("Your age is " + str(age))

# floats
price = 50.25
print("The price for this snack is " + str(price) + " PHP")

# boolean
human = True
print("Are you a human? " + str(human))

# multiple assignment
name2, age2, price2 = "Ellie", 5, 23.15
print("Dog's name: " + name2 + ", age: " + str(age2) + ", dog food: " + str(price2) + " per kg.")

# printing the data type
print(type(name))
print(type(age))
print(type(price))
print(type(human))
